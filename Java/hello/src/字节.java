public class 字节 {
    public static void main(String[] args) {
        byte b1=45,b2,b3;  //定义三个字节型变量，并初始化第一个
        short s1=765;
        int i = 6, j, k;
        char c1='好';
        long l1 = 1000;
        float f1 = 34.2f;//定义单精度浮点型变量，必须加后缀“f”。否则，系统默认为双精度类型
        double d1=45.6;
        boolean bool = true;
        System.out.println("字节整型变量b1的值是："+b1);
        System.out.println("短整型变量s1的值是："+s1);
        System.out.println("整型变量i的值是："+i);
        System.out.println("长整型变量l1的值是："+l1);
        System.out.println("字符型变量c1的值是："+c1);
        System.out.println("单精度型变量f1的值是："+f1);
        System.out.println("双精度型变量d1的值是："+d1);
        System.out.println("布尔型变量bool的值是："+bool);
    }
}
