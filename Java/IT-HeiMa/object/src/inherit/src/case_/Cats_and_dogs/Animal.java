package case_.Cats_and_dogs;

public class Animal {
   public String name;
   public int age;
//无参
    public Animal() {
    }
//带参
    public Animal(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
